package net.parim.sns.modules.gen.service;

import net.parim.sns.common.service.BaseService;
import net.parim.sns.common.utils.StringUtils;
import net.parim.sns.modules.gen.dao.GenTemplateDao;
import net.parim.sns.modules.gen.entity.GenTemplate;

import org.apache.commons.lang3.StringEscapeUtils;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.data.domain.Page;
import org.springframework.data.domain.Pageable;
import org.springframework.stereotype.Service;
import org.springframework.transaction.annotation.Transactional;

/**
 * 代码模板Service
 * @author ThinkGem
 * @version 2013-10-15
 */
@Service
@Transactional(readOnly = true)
public class GenTemplateService extends BaseService {

	@Autowired
	private GenTemplateDao genTemplateDao;
	
	public GenTemplate get(Long id) {
		return genTemplateDao.findOne(id);
	}
	
	public Page<GenTemplate> find( GenTemplate genTemplate, Pageable pageable) {
		return (Page<GenTemplate>)genTemplateDao.findAll(genTemplate);
	}
	
	@Transactional(readOnly = false)
	public void save(GenTemplate genTemplate) {
		if (genTemplate.getContent()!=null){
			genTemplate.setContent(StringEscapeUtils.unescapeHtml4(genTemplate.getContent()));
		}
		if (StringUtils.isBlank(genTemplate.getId())){
			genTemplateDao.insert(genTemplate);
		}else{
			genTemplateDao.update(genTemplate);
		}
	}
	
	@Transactional(readOnly = false)
	public void delete(GenTemplate genTemplate) {
		genTemplateDao.delete(genTemplate);
	}
	
}
